class BinaryNode:
    def __init__(self, elem: int, node_left: 'BinaryNode' = None, node_right: 'BinaryNode' = None) -> None:
        self.elem = elem
        self.left = node_left
        self.right = node_right


class MyBinarySearchTree:
    def __init__(self) -> None:
        """creates an empty binary tree
        I only has an attribute: _root"""
        self._root = None

    def draw(self) -> None:
        """function to draw a tree. """
        if self._root:
            self._draw('', self._root, False)
        else:
            print('tree is empty')
        print('\n\n')

    def _draw(self, prefix: str, node: BinaryNode, is_left: bool) -> None:
        if node is not None:
            self._draw(prefix + "     ", node.right, False)
            print(prefix + "|-- " + str(node.elem))
            self._draw(prefix + "     ", node.left, True)


    def insert(self, elem: object) -> None:
        self._root = self._insert(self._root, elem)

    def _insert(self, node: BinaryNode, elem: object) -> BinaryNode:
        if node is None:
            return BinaryNode(elem)

        if node.elem == elem:
            print('Error: elem already exist ', elem)
            return node

        if elem < node.elem:
            node.left = self._insert(node.left, elem)
        else:
            # elem>node.elem
            node.right = self._insert(node.right, elem)
        return node

    # Removes all leaf nodes having value outside the given range
    # returns a sorted list in ascending order
    def removeOutsideRange(self, min: int, max: int) -> []:
        if self._root is not None:
            if (self._root.left or self._root.right):
                return self._removeOutsideRange(min, max, self._root, [])
            elif (self._root.elem <= min or self._root.elem >= max):
                elem = self._root.elem
                self._root = None
                return [elem]
            else:
                return []
        else:
            return []

    def _removeOutsideRange(self, min: int, max: int, nodo:BinaryNode, lista) -> []:
        if nodo.left and (not nodo.left.left and not nodo.left.right) and (nodo.left.elem < min or nodo.left.elem > max):
            lista.append(nodo.left.elem)
            nodo.left = None
        elif nodo.left and (nodo.left.left or nodo.left.right):
            self._removeOutsideRange(min, max, nodo.left, lista)

        if nodo.right and (not nodo.right.left and not nodo.right.right) and (nodo.right.elem < min or nodo.right.elem > max):
            lista.append(nodo.right.elem)
            nodo.right = None
        elif nodo.right and (nodo.right.left or nodo.right.right):
            self._removeOutsideRange(min, max, nodo.right, lista)

        return lista



